# while loop
x = 1                #initial value this variable is called iterators , and running in loop is called iteration
while x<=2:           # condition
    print("hello world")        # print statement
    x=x+1                       # increment operator
print(x)

# print first n natural numbers 
n = int(input("enter number : "))
i = 1
while i<= n :
    print(i)
    i+=1
print("!----loop ended-----!")

